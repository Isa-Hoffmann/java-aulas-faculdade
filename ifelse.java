public class ifelse {
    public static void main(String[] args) {

        int num = 24;

        int resto = num % 2;

        if(resto == 0){
            System.out.println("Par");
        }else{
            System.out.println("Impar");
        }
        
        
    }
    //if = se (faço algo)
    //else = se não (faço outro algo)
    // = em programação - ==
    // módulo (resto da divisão- verificar se é par ou impar) = %
     
}
